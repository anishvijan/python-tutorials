#This is variable in python

a = 10              #declaring single variable
print(a)            #printing the variable

a, b, c = 1, 2, 3   #declaring multiple variable in single line
print(a)            #printing first variable
print(b)            #printing second variable
print(c)            #printing third variable

a = b = c = 13      #declaring all variable with same value
print(a)            #printing first variable
print(b)            #printing second variable
print(c)            #printing third variable
